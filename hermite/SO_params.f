C***********************************************************************
C
C
      FUNCTION SO_rh()
C
C
C***********************************************************************
      INCLUDE 'hermite.h'

      SO_rh = r200*(0.6082 - 0.1843*LOG(concentration) -
     &      0.1011*(LOG(concentration)**2.) +
     &      0.03918*(LOG(concentration)**3.))
      RETURN
      END

C***********************************************************************
C
C
      FUNCTION rho_c()
C
C
C***********************************************************************
      INCLUDE 'hermite.h'

      rho_c = Ms*(SO_rh+rc)/(2.*PI**2.*rc**2.*SO_rh**2.)
      RETURN
      END

C***********************************************************************
C
C
      FUNCTION SO_rho(r)
C
C
C***********************************************************************

      INCLUDE 'hermite.h'

      SO_rho = rho_c/((1+r**2/rc**2)*(1+r**2/SO_rh**2))
      RETURN
      END
